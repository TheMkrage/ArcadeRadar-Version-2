    override func tableView(_ tableView: UITableView, didSelectRowAt indexPath: IndexPath) {
        guard let vc = UIStoryboard(name: "Main", bundle: nil).instantiateViewController(withIdentifier: "ArcadeTabProfile") as? ArcadeTabViewController else {
            return
        }
        vc.arcade = arcades[indexPath.row]
        vc.view.backgroundColor = UIColor.white
        vc.view.layer.borderWidth = 0
        let presentr = Presentr(presentationType: .custom(width: .sideMargin(value: 25), height: .fluid(percentage: 0.8), center: .center))
        let navigationController = UINavigationController(rootViewController: vc)
        self.customPresentViewController(presentr, viewController: navigationController, animated: true, completion: nil)
    }

    override func numberOfSections(in tableView: UITableView) -> Int {
        return 1
    }

    override func tableView(_ tableView: UITableView, numberOfRowsInSection section: Int) -> Int {
        return arcades.count
    }

    override func tableView(_ tableView: UITableView, cellForRowAt indexPath: IndexPath) -> UITableViewCell {
        let cell = UITableViewCell()
        cell.textLabel?.text = self.arcades[indexPath.row].name
        return cell
    }

